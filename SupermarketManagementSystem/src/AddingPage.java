    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try {
            stmt = con.createStatement();
            
            int Id = Integer.parseInt(id.getText());
            
            String query = "SELECT * FROM items";
            
            rs = stmt.executeQuery(query);
            
            while(rs.next())
            {
                if(Id == rs.getInt("id"))
                {
                    iname.setText(rs.getString("iname"));
                    icatagory.setText(rs.getString("icatagory"));
                    iprice.setText(String.format("%s",rs.getInt("iprice")));
                    icode.setText(String.format("%s",rs.getInt("icode")));
                    istore.setSelectedItem(rs.getString("istore"));
                    idate.setText(rs.getString("idate"));
                }
            }
            
            
            
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_searchActionPerformed

