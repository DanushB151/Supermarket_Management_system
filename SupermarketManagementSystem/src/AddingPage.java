    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        try {
            stmt = con.createStatement();
            
            int Id = Integer.parseInt(id.getText());
            String iName = iname.getText();
            String iCatagory = icatagory.getText();
            int iPrice = Integer.parseInt(iprice.getText());
            int iCode = Integer.parseInt(icode.getText());
            String iStore = (String)istore.getSelectedItem();
            String iDate = idate.getText();
            
            String query;
            query = "UPDATE items SET iname = ?, icatagory = ?, iprice = ?, icode = ?, istore = ?, idate = ? WHERE id = ?";
            
            PreparedStatement preparedStmt = con.prepareStatement(query);
            
            preparedStmt.setString(1, iName);
            preparedStmt.setString(2, iCatagory);
            preparedStmt.setInt(3, iPrice);
            preparedStmt.setInt(4, iCode);
            preparedStmt.setString(5, iStore);
            preparedStmt.setString(6, iDate);
            preparedStmt.setInt(7, Id);
            
            preparedStmt.executeUpdate();
            JOptionPane.showMessageDialog(null,"Successfully Updated");
            
        } catch (Exception e) {
            System.out.println(e);
        }
        
        
         
        
        
    }//GEN-LAST:event_updateActionPerformed

